package com.ust;

public class Switch {
	public static void main(String[] args) {
		int expression=2;
		switch(expression) {
		case 1:
			System.out.println("case 1");
			System.out.println("case 2");
			System.out.println("case 3");
			default:
				System.out.println("Default case");
		}
		
	}

}
