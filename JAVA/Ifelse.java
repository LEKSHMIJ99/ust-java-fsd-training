package com.ust;

public class Ifelse {
	public static void main(String[] args) {
		int number=10;
		//checks if no is greater than 0
		if(number>0) {
			System.out.println("the no is positive");
			
		}
		//executive this block
		//if no is not greater than 0
		else {
			System.out.println("the no is not positive");
		}
		System.out.println("statement outside if...else block");
		
	}

}

