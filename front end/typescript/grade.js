var grade = "A";
switch (grade) {
    case "A": {
        console.log("excellent");
        break;
    }
    case "B": {
        console.log("good");
        break;
    }
    case "C": {
        console.log("fair");
        break;
    }
    case "D": {
        console.log("poor");
        break;
    }
    default: {
        console.log("invalid choice");
        break;
    }
}
